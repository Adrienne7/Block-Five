package com.bayviewglen.daythree;

public class Homework1 {

	public static void main(String[] args) {
		double area, radius;
		
		radius = 3;
		
		area = 3.14 * radius * radius;
		
		System.out.println("The area of the circle with radius " + radius + " is " + area + ".");
	}

}
